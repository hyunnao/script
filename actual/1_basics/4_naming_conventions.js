/**
 * Naming Convention 1
 * 
 * 변수이름 지을떄
 * 1) 일반적으로 영어(라틴문자)를 사용하여 문자와 숫자를 모두 사용할 수 있다.
 * 2) 특수기호는 언더스코어(_)와 달러($)를 사용할 수 있다.
 * 3) 숫자로 이름을 시작할수 없다.
 *    1Name, 2Hello
 * 4) 키워드는 변수명으로 사용할 수 없다.
 *    var var/let/const = 'var'; (X)
 */

